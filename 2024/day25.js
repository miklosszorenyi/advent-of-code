function getsize(block, type) {
  const sizes = [];
  if (type === 'lock') {
    block = block.reverse();
  }

  for (let i = 0; i < block[0].length; i++) {
    sizes.push(0);
  }

  for (let i = 1; i < block.length; i++) {
    for (let j = 0; j < block[i].length; j++) {
      if (!sizes[j]) {
        sizes[j] = 0;
      }
      if (block[i][j] === '#') {
        sizes[j]++;
      }
    }
  }
  return sizes;
}

function part1(input) {
  const keys = [];
  const locks = [];

  for (let i in input) {
    if (input[i][0].split('').filter(b => b == '.').length === 0) {
      keys.push(getsize(input[i], 'key'));
    } else {
      locks.push(getsize(input[i], 'lock'));
    }
  }

  let fitted = 0;
  for (let key of keys) {
    for (let lock of locks) {
      if (key.map((k, i) => k + lock[i]).filter(k => k <= 5).length === 5) {
        fitted++;
      }
    }
  }

  return fitted;
}

